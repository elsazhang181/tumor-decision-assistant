// ============== API 路由 ==============
export async function POST(request: NextRequest) {
  try {
    const { message, stage = 'symptom', history = [], context } = await request.json();
    
    const stagePrompt = STAGE_PROMPTS[stage as Stage] || STAGE_PROMPTS.symptom;
    
    // 如果有上下文，添加到 system prompt 中
    const contextPrompt = context?.summary 
      ? `\n\n## 📋 前序环节结论摘要\n${context.summary}\n\n**【重要】上述内容为用户在其他环节已获得的结论，回答时请：**
**1. 直接引用上述结论作为【结论】**
**2. 仅在【通俗解释】和【依据】部分补充当前环节特有的信息**
**3. 核心判断必须与上述结论保持完全一致，不得矛盾**`
      : '';
    
    const messages = [
      { role: 'system', content: stagePrompt + contextPrompt },
      ...history.map((h: { role: string; content: string }) => ({ role: h.role, content: h.content })),
      { role: 'user', content: message }
    ];
    
    const config = new Config();
    const client = new LLMClient(config);
    
    const customHeaders = HeaderUtils.extractForwardHeaders(request.headers);
    
    const encoder = new TextEncoder();
    const stream = new ReadableStream({
      async start(controller) {
        let isClosed = false;
        
        const closeStream = () => {
          if (!isClosed) {
            isClosed = true;
            try {
              controller.close();
            } catch {
              // Ignore close errors
            }
          }
        };
        
        try {
          const formattedMessages = messages.map(m => ({
            role: m.role as 'user' | 'assistant' | 'system',
            content: m.content
          }));
          
          const responseStream = client.stream(
            formattedMessages,
            { streaming: true },
            undefined,
            customHeaders
          );
          
          for await (const part of responseStream) {
            const content = part.content;
            if (content && typeof content === 'string') {
              const data = JSON.stringify({
                content,
                stage
              });
              
              try {
                controller.enqueue(encoder.encode(`data: ${data}\n\n`));
              } catch {
                break;
              }
            }
          }
          
          closeStream();
        } catch (error: unknown) {
          const err = error instanceof Error ? error : new Error(String(error));
          console.error('LLM stream error:', error);
          
          const errorMsg = JSON.stringify({
            content: `抱歉，服务遇到问题：${err.message || '请稍后重试'}`,
            stage,
            isError: true
          });
          
          try {
            controller.enqueue(encoder.encode(`data: ${errorMsg}\n\n`));
          } catch {
            // Ignore
          }
          
          closeStream();
        }
      }
    });
    
    return new Response(stream, {
      headers: {
        'Content-Type': 'text/event-stream',
        'Cache-Control': 'no-cache',
        'Connection': 'keep-alive'
      }
    });
  } catch (error: unknown) {
    const err = error instanceof Error ? error : new Error(String(error));
    console.error('API error:', error);
    return NextResponse.json(
      { error: '请求处理失败', message: err.message },
      { status: 500 }
    );
  }
}
